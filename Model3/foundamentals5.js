function getGreeting(name) {
return 'Hello ' + name + '!';
}
console.log(getGreeting("Erson"));
const sayHiExpression = function(name) {
return 'Hello ' + name + '!';
}
console.log(sayHiExpression("Erson"));

const sayHiArrow = (name) => 'Hello ' + name + '!'; // arrow function syntax, more concise
console.log(sayHiArrow("Erson"));